from effective_features import run, ExperimentConfig


# =========================================================================
# ГОТОВЫЕ РЕЖИМЫ (пресеты)
# =========================================================================
PRESETS = {
    # Быстрый — для отладки. Проверить, что всё читается и считается.
    'fast':     ExperimentConfig(n_patches=10, max_pixels_total=120_000),

    # Исследовательский — основной режим для практики и экспериментов.
    'research': ExperimentConfig(n_patches=50),

    # Полный — весь датасет, для финального результата в отчёт.
    'full':     ExperimentConfig(),   # n_patches=None → весь датасет
}


if __name__ == "__main__":
    # =====================================================================
    # ВЫБЕРИ РЕЖИМ: 'fast' / 'research' / 'full'
    # =====================================================================
    MODE = 'fast'

    print(f"\n  Режим запуска: '{MODE}'\n")
    run(PRESETS[MODE])
